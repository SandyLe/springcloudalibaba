package org.jeecg.common.enums;

/**
 * 工单类型
 */
public enum WorkType implements AbstractEnum {

    Measure(0, "Measure", "测量工单"),
    Install(1, "Install", "安装工单"),
    Repair(2, "Repair", "维修工单")
    ;

    private Integer id;
    private String code;
    private String name;
    WorkType(Integer id, String code, String name){
        this.id = id;
        this.code = code;
        this.name = name;
    }

    @Override
    public String getSid() {
        return String.valueOf(id);
    }
    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    @Override
    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public static String getName(Integer id) {
        WorkType[] values = WorkType.values();
        for (WorkType o: values){
            if(o.getMap().get("id").equals(id)){
                return o.getMap().get("name").toString();
            }
        }
        return null;
    }

    public static String getName(String code) {
        WorkType[] values = WorkType.values();
        for (WorkType o: values){
            if(o.getMap().get("code").equals(code)){
                return o.getMap().get("name").toString();
            }
        }
        return null;
    }

    public static String getCode(Integer id) {
        WorkType[] values = WorkType.values();
        for (WorkType o: values){
            if(o.getMap().get("id").equals(id)){
                return o.getMap().get("code").toString();
            }
        }
        return null;
    }
}
